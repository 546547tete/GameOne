package com.example.bean;

public class NumBerDateBean {

    /**
     * num : 61
     * color : 2E2B5F
     * shape : 4
     * x : 50
     * y : 250
     * radius : 50
     */

    private int num;
    private String color;
    private int shape;
    private int x;
    private int y;
    private int radius;

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getShape() {
        return shape;
    }

    public void setShape(int shape) {
        this.shape = shape;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
